# Important hubs receiving traffic from other important hubs rank higher.
#therefore : weight ="trip_volume"
pagerank_n    = nx.pagerank(G, weight="trip_volume", alpha=0.85)

# Clustering Coefficient:
# Measures how connected a hub's neighbours are.
# High value = dense local mesh with alternate routes.
clustering_n  = nx.clustering(G_und, weight="weight")

# Edge Betweenness Centrality:
# Identifies critical corridors.
