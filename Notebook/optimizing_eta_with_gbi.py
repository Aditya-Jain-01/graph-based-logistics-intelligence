for epoch in range(SAGE_EPOCHS):
    model_sage.train()
    optimizer.zero_grad()

    z = model_sage(data_dev.x, data_dev.edge_index)            # (N, out_dim)

    pos_idx = torch.randint(0, n_edges_total, (BATCH_EDGES,), device=device)
    pos_src = data_dev.edge_index[0, pos_idx]
    pos_dst = data_dev.edge_index[1, pos_idx]

    neg_src = torch.randint(0, N_NODES, (BATCH_EDGES * NEG_RATIO,), device=device)
    neg_dst = torch.randint(0, N_NODES, (BATCH_EDGES * NEG_RATIO,), device=device)

