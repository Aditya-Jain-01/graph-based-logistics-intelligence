def _expected_eta(src, dst, hour, day_of_week, route_type):
    base = {
        "segment_osrm_time"     : 0,
        "segment_osrm_distance" : 0,
        "osrm_speed_kmh"        : 40,
        "departure_hour"        : hour,
        "hour_sin"              : np.sin(2*np.pi*hour/24),
        "hour_cos"              : np.cos(2*np.pi*hour/24),
        "is_weekend"            : int(day_of_week >= 5),
        "is_night_shipment"     : int(hour < 6 or hour >= 22),
        "route_type_encoded"    : int(route_type == "FTL"),
        "departure_day_of_week" : day_of_week,
        "cutoff_factor"         : 1.0,
        "is_cutoff"             : 0,
    }
    cid  = f"{src}_{dst}_{route_type}"
    crow = corridor_stats[corridor_stats["corridor_id"] == cid]
    if len(crow):
        base.update({
            "corridor_median_delay": float(crow["corridor_median_delay"].iloc[0]),
            "corridor_mean_delay"  : float(crow["corridor_mean_delay"].iloc[0]),
            "corridor_delay_std"   : float(crow["corridor_delay_std"].iloc[0] or 0),
            "corridor_trip_volume" : float(crow["corridor_trip_volume"].iloc[0]),
            "corridor_pct_delayed" : float(crow["corridor_pct_delayed"].iloc[0]),
        })
    else:
        for c in ["corridor_median_delay","corridor_mean_delay","corridor_delay_std",
                  "corridor_trip_volume","corridor_pct_delayed"]:
            base[c] = float(corridor_stats[c].median())

    for name, lookup in CENTRALITY_LOOKUPS.items():
        base[f"src_{name}"] = float(lookup.get(src, 0))
        base[f"dst_{name}"] = float(lookup.get(dst, 0))

    src_n2v      = n2v_emb.get(src,      np.zeros(EMBEDDING_DIM))
    dst_n2v      = n2v_emb.get(dst,      np.zeros(EMBEDDING_DIM))
    src_sage     = sage_emb.get(src,     np.zeros(SAGE_OUT))
    dst_sage     = sage_emb.get(dst,     np.zeros(SAGE_OUT))
    src_sup_sage = sup_sage_emb.get(src, np.zeros(SAGE_OUT))
    dst_sup_sage = sup_sage_emb.get(dst, np.zeros(SAGE_OUT))
    for i in range(EMBEDDING_DIM):
        base[f"src_n2v_{i}"]      = float(src_n2v[i])
        base[f"dst_n2v_{i}"]      = float(dst_n2v[i])
    for i in range(SAGE_OUT):
        base[f"src_sage_{i}"]     = float(src_sage[i])
        base[f"dst_sage_{i}"]     = float(dst_sage[i])
        base[f"src_sup_sage_{i}"] = float(src_sup_sage[i])
        base[f"dst_sup_sage_{i}"] = float(dst_sup_sage[i])
    # Cosine similarities
    def _cos(a, b):
        na, nb = np.linalg.norm(a), np.linalg.norm(b)
        if na < 1e-8 or nb < 1e-8: return 0.0
        return float(np.dot(a, b) / (na * nb))
    base["cos_sim_n2v"]      = _cos(src_n2v,      dst_n2v)
    base["cos_sim_sage"]     = _cos(src_sage,     dst_sage)
    base["cos_sim_sup_sage"] = _cos(src_sup_sage, dst_sup_sage)
    row = np.array([base[c] for c in SUP_SAGE_FEATURES]).reshape(1, -1)
    return float(models[PRIMARY_MODEL_NAME].predict(row)[0])


def recommend(src, dst, hour=10, day_of_week=2):
    have_both = ((pivot["src"] == src) & (pivot["dst"] == dst)).any()
    if have_both:
        prow  = pivot[(pivot["src"] == src) & (pivot["dst"] == dst)].iloc[0]
        x_clf = prow[FW_FEATURES].values.reshape(1, -1).astype(float)
        ftl_prob = float(clf.predict_proba(x_clf)[0, 1])
        ftl_pick = ftl_prob >= 0.5
    else:
        ftl_only     = (corridor_stats["corridor_id"] == f"{src}_{dst}_FTL").any()
        carting_only = (corridor_stats["corridor_id"] == f"{src}_{dst}_Carting").any()
        if   ftl_only and not carting_only:    ftl_pick, ftl_prob = True,  0.55
        elif carting_only and not ftl_only:    ftl_pick, ftl_prob = False, 0.55
        else:
            ftl_pick = betweenness_n.get(src, 0) > np.median(list(betweenness_n.values()))
            ftl_prob = 0.55 if ftl_pick else 0.45

    rec   = "FTL" if ftl_pick else "Carting"
    eta_m = _expected_eta(src, dst, hour, day_of_week, rec)
    cid   = f"{src}_{dst}_{rec}"
    crow  = corridor_stats[corridor_stats["corridor_id"] == cid]
    risk  = (float(crow["corridor_pct_delayed"].iloc[0])
             if len(crow) else float(corridor_stats["corridor_pct_delayed"].median()))

    return {
        "src"                 : src,
        "dst"                 : dst,
        "hour"                : hour,
        "day_of_week"         : day_of_week,
        "recommended_route"   : rec,
        "confidence"          : round(max(ftl_prob, 1 - ftl_prob), 3),
        "expected_eta_minutes": round(eta_m, 1),
        "expected_delay_risk" : round(risk, 3),
        "model_used"          : PRIMARY_MODEL_NAME,
    }

